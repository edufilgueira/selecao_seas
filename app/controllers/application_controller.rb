    str = str.gsub(/[ÇĆĈĊČ]/, 'C')
    str = str.gsub(/[çćĉċč]/, 'c')
    str = str.gsub(/[ÐĎ]/, 'D')
    str = str.gsub(/[ďđ]/, 'd')
    str = str.gsub(/[ÈÉÊËĒĔĖĘĚ]/, 'E')
    str = str.gsub(/[èéêëēĕėęě]/, 'e')
    str = str.gsub(/[ĝğġģ]/, 'g')
    str = str.gsub(/[ĜĞĠĢ]/, 'G')
    str = str.gsub(/[ĥħ]/, 'h')
    str = str.gsub(/[ĤĦ]/, 'H')
    str = str.gsub(/[ìíîïĩīĭįı]/, 'i')
    str = str.gsub(/[ÌÍÎÏĨĪĬĮİ]/, 'I')
    str = str.gsub(/[Ĵ]/, 'J')
    str = str.gsub(/[ĵ]/, 'j')
    str = str.gsub(/[Ķķ]/, 'K')
    str = str.gsub(/[ĸ]/, 'k')
    str = str.gsub(/[ĺļ]/, 'l')
    str = str.gsub(/[ĹĻĽľĿŀŁł]/, 'L')
    str = str.gsub(/[ñńņňŉŋ]/, 'n')
    str = str.gsub(/[ÑŃŅŇŊ]/, 'N')

    str = str.gsub(/[òóôõöøōŏő]/, 'o')
    str = str.gsub(/[ŕŗř]/, 'r')
    str = str.gsub(/[ŔŖŘ]/, 'B')
    str = str.gsub(/[śŝşš]/, 's')
    str = str.gsub(/[ŚŜŞŠ]/, 'S')
    str = str.gsub(/[ſ]/, 'f')
    str = str.gsub(/[ţťŧ]/, 't')
    str = str.gsub(/[ŢŤŦ]/, 'T')
    str = str.gsub(/[ùúûüũūŭůűų]/, 'u')
    str = str.gsub(/[ÙÚÛÜŨŪŬŮŰŲ]/, 'U')
    str = str.gsub(/[Ŵ]/, 'W')
    str = str.gsub(/[ŵ]/, 'w')
    str = str.gsub(/[ýÿŷ]/, 'y')
    str = str.gsub(/[ÝŶŸ]/, 'Y')
    str = str.gsub(/[źž]/, 'z')
    str = str.gsub(/[ŹŻŽ]/, 'Z')
    str = str.gsub(/['+."!@#$%¨&*()-=]/, '')
    str = str.gsub(/^a-zA-Z0-9_/, '')
    str = str.gsub(/[(){}<>?,;:~^´`]/, '')
    str = str.gsub(/['"!@#$%¨&*+=§°º]/, '')
    str = str.gsub(/[ -]/, '_')
    str = str.gsub("[", '')
    str = str.gsub("]", '')
    str = str.gsub("__", '_')
    str = str.gsub("___", '_')
    str = str.gsub("____", '_')
    str = str.gsub("_____", '_')
  end
end
